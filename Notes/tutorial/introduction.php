            <h3>History of JavaScript</h3>
            <p class="para">
                In 1993, <b>Mosaic</b>, the first popular web browser, came into existence. In the <b> year 1994, Netscape</b> was founded by <b>Marc Andreessen</b>. 
                He realized that the web needed to become more dynamic. Thus, a 'glue language' was believed to be provided to HTML to make web designing easy for designers and part-time programmers.
                 Consequently, in 1995, the company recruited <b>Brendan Eich</b> intending to implement and embed Scheme programming language to the browser. But, before Brendan could start,
                  the company merged with <b> Sun Microsystems</b> for adding Java into its Navigator so that it could compete with Microsoft over the web technologies and platforms. Now, two languages were there:
                   Java and the scripting language. Further, Netscape decided to give a similar name to the scripting language as Java's. It led to 'Javascript'. Finally, in May 1995, Marc Andreessen coined
                    the first code of Javascript named <b>'Mocha'</b> . Later, the marketing team replaced the name with <b>'LiveScript'</b> . 
                But, due to trademark reasons and certain other reasons, in December 1995, the language was finally renamed to 'JavaScript'. From then, JavaScript came into existence.
            </p>
            

